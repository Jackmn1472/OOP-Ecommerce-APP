    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        if(AppData.productcount == 0)
        {
            lstBasket.setEnabled(false);
            return;
        }// end of if
        
        lstBasket.setEnabled(true);
        listModel.clear();
        
        for(int index=0; index < AppData.productcount; index ++)
        {
            listModel.addElement(AppData.products[index].getItemName() + " " + AppData.products[index].getItemPrice());
        }// end of for
    }//GEN-LAST:event_formWindowActivated

    
